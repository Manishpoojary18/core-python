# Variable - variable is a named memory loaction that can hold single valued data
# or variable is a container to store a value

# Rules for naming a variable
# 1.Variable name should starts with either underscore or character
# 2.variable name contains A-Z,a-z,0-9
# 3.variable name cant contains only digits
# 4.keywords are not allowed
# 5.spaces are not allowed
# 6.special symbols are not allowed

# Datatypes
# 1.Numeric d.t-int,float,complex
# 2.string d.t-str
# 3.sequence d.t-list,tuple,range
# 4.mapping d.t-dict
# 5.set d.t-set,frozenset
# 6.binary d.t-bytes,bytearray,memoryview
# 7.Boolean d.t


# why python is called as a dynamically typed programming language
# -->we dont need to declare the datatypes of the variable explicitly
# -->It identifies the type of the data storing in the variable during the execution time
